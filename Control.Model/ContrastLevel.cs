namespace Buttercup.Control.Model
{
    /// <summary>
    /// Enumerates the contrast options for the rendered text content.
    /// </summary>
    public enum ContrastLevel
    {
        /// <summary>
        /// Default contrast scheme - Black text on white background
        /// </summary>
        BlackTextOnWhite,
        /// <summary>
        /// White text on dark blue background
        /// </summary>
        
        WhiteTextOnBlue,
        /// <summary>
        /// Yellow text on black background
        /// </summary>
        YellowTextOnBlack
    }
}